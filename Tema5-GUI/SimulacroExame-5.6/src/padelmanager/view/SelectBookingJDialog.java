    /**
     * Establece unha nova lista das reservas do dia escollido as que se pode
     * unir o xogador
     *
     * @param bookings lista das reservas do dia escollido as que se pode unir o
     * xogador
     */
    public void setBookings(ArrayList<Booking> bookings) {
        this.bookings = bookings;

        DefaultListModel listModel = new DefaultListModel();
        jListBookings.setModel(listModel);
        for (Booking bk : this.bookings) {
            listModel.addElement(bk.getDescription());
        }
    }

    /**
     *
     * @return a reserva seleccionada polo usuario
     */
    public Booking getSelectedBooking() {
        return bookings.get(jListBookings.getSelectedIndex());
    }

