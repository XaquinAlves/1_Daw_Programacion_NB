    /**
     * Accion realizada ao pulsar o boton Aceptar
     *
     * @param evt
     */
    private void jButtonAceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptActionPerformed
        if (jListBookings.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar unha reserva");
        } else {
            if (bookings.get(jListBookings.getSelectedIndex()) instanceof DoubleBooking) {
                selectedTeam = Integer.parseInt(JOptionPane.showInputDialog(this, "Indica o equipo ao que te queres apuntar(1/2)")) - 1;
            }
            actionAccepted = true;
            this.dispose();
        }
    }//GEN-LAST:event_jButtonAceptActionPerformed
