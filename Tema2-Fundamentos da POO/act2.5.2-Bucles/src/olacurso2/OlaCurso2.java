package olacurso2;
/**
 * Este programa mostra por pantalla "Ola Curso!" 7 veces usando un bucle for
 * @author Xaquin Alves Gonzalez
 */
public class OlaCurso2 {
    
    //Facemos un bucle for que empece co contador en 0 e acabe en 6, o que da 7 iteracions
    public static void main(String[] args) {
        for(int i=0; i<7; i++){
            System.out.println("Ola curso!");
        }
    }
    
}

