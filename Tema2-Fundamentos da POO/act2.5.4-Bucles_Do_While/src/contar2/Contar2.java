package contar2;

/**
 * Este programa mostra por pantalla os 10 primeiros numeros(0-9) usando un bucle do while
 * @author Xaquin Alves Gonzalez
 */
public class Contar2 {

    public static void main(String[] args) {
        int number = 0;
        
        do{
            System.out.println(number);
            number ++;
        }while(number < 10);
    }
    
}
