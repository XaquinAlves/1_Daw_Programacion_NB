package contar;

/**
 * Este programa mostra por pantalla os 10 primeiros numeros(0-9) usando un bucle for
 * @author Xaquin Alves Gonzalez
 */
public class Contar {

    public static void main(String[] args) {

        for(int i=0;i < 10;i++){
            System.out.println(i);
        }
    }
    
}

