package tabla5;

/**
 * Este programa mostra por pantalla a tabla de multiplicar do 5, facendo uso
 * dun bucle for
 *
 * @author Xaquin Alves Gonzalez
 */
public class Tabla5 {

    public static void main(String[] args) {
        for(int i=0;i <11;i++){
            System.out.println("5 x "+i+" = "+i*5);
        }
    }

}

